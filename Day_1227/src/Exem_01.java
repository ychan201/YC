
public class Exem_01 {

	public static void main(String[] args) {
		int a=5;
		System.out.println("a=" + a++);
		System.out.println("a=" + a--);
		System.out.println("a=" + -a);
		System.out.println("a=" + ~a); // 이게 보수임. 그래서 -6 플라스1로 한 결과값.
		System.out.println("a=" + (float)a);
		System.out.println("a=" + (a>3));
		System.out.println("a=" + !(a>3));


	}

}
