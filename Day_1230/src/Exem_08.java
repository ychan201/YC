//정수 1개 입력.
// 'A' ~ 'F' 판단하기.

public class Exem_08 {
	public static void main(String[] args) {
		int a=80;
		if(a>=90)
			System.out.println("A");
		else if(a>=80)
			
			System.out.println("B");
		else if(a>=70)
			System.out.println("C");
		else if(a>=60)
			System.out.println("D");
		else
			System.out.println("F");
		

		

	}

}
